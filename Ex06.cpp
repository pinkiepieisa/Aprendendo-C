#include <iostream>
using namespace std;

int main () {
    cout << 12;
    // Não se faz uso de "" em números, ou vira String
    cout << "\n";
    cout << 11 + 2;
    cout << "\n";
    cout << 1 - 2 << "\n";
    // Mesmo esquema do de cima, porém reduzido
    cout << 2 * 4 << "\n\n";
    // Cria uma linha em branco
    cout << 4/2 << endl;
    // Outra forma de pular linha
    cout << "Testando formas de pular a linha!";
    return 0;
}